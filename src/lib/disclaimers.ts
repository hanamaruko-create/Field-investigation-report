function normalizeCode(code: string): string {
  return code.trim().toUpperCase();
}

export function buildDisclaimerTextFromCodes(codes: string[]) {
  const texts = codes
    .map((c) => normalizeCode(c))
    .filter((c): c is DisclaimerCode => c in DISCLAIMER_TEXT_BY_CODE)
    .map((c) => DISCLAIMER_TEXT_BY_CODE[c])
    .filter(Boolean);
  return texts.join("\n\n");
}

